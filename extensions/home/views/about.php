<p>ISU Garage Sale is a website designed and maintained by students at Iowa State University! We created this website to allow students a safe way to buy, sell, and trade their items!</p>
<h3>Why use ISU Garage Sale?</h3>
<ul>
	<li>Buy locally: no more waiting for shipping</li>
	<li>Only Iowa State students allowed: no more meetings with strangers from Craigslist</li>
</ul>

